#include "libft.h"

int	ft_isalnum(int nm)
{
	if (((nm <= 'z' && nm >= 'a') || (nm <= 'Z' && nm >= 'A'))
		|| (nm <= '9' && nm >= '0'))
		return (1);
	return (0);
}
